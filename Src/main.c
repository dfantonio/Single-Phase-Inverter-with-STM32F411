/**
 * @brief  The application entry point.
 *
 * @retval None
 */
int main(void) {
	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration----------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART2_UART_Init();
	MX_TIM1_Init();
	MX_TIM11_Init();
	/* USER CODE BEGIN 2 */

	char freq[5];
	char texto[50];
	int freq_int, size;
	int cont = 0;

	//	Generate an array of points to use as the CCR of the TIM1
	for (float aux = 0; aux < 360; aux += (360. / 195.), cont++) {
		tabela_valores[cont] = (sin(aux * M_PI / 180) * 75) + 98;
	}

	//	Initialize the TIM11 with interruption
	HAL_TIM_Base_Start_IT(&htim11);

	/*	Starts the TIM1 in PWM mode, operating with channel 1 and channel 1
	 *  complementary.
	 *  This complementary channel is basically the PWM but inverted.
	 */
	HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start_IT(&htim1, TIM_CHANNEL_1);

	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, tabela_valores[0]);
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {

		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */

		/*	Checks if the ARM received some value through the serial to
		 *  update the frequency of TIM11
		 */
		if (HAL_UART_Receive(&huart2, freq, 3, 1) == HAL_OK) {

			freq_int = atoi(freq);
			size = sprintf(texto, "Frequência escolhida: %d\n", freq_int);
			HAL_UART_Transmit(&huart2, texto, size, 100);

			__HAL_TIM_SET_PRESCALER(&htim11, 6000 / freq_int);

		}
	}
	/* USER CODE END 3 */

}

